import { Schema, model } from "mongoose";
import { ICart } from "../types/cart";

const CartSchema = new Schema(
  {
    userId: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    items: [
      {
        productId: {
          type: Schema.Types.ObjectId,
          ref: "Product",
          required: true,
        },
        quantity: {
          type: Number,
          required: true,
        },
      },
    ],
    totalPrice: {
      type: Number,
      required: true,
      default: 0,
    },
  },
  { collection: "carts", timestamps: true }
);

const Cart = model<ICart>("Cart", CartSchema);
export default Cart;
